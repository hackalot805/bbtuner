	public void findAndIsolateBand(int arraySize) {
		//
		// 1. Find overall maximum
		// 2. take percentage of max, and find a range around the first local
		// maximum.
		// 3. Zero out all other values
		//
		float maxValue = 0;
		int indexOfMax = 0;

		for (int count = 0; count < arraySize; count++) {
			if (m_lpFFT_Out[count] > maxValue) {
				maxValue = m_lpFFT_Out[count];
				indexOfMax = count;
			}
		}

		//
		// Step 2 & 3.
		//
		float cutoffValue = maxValue * 0.30f;
		int state = 0;
		for (int count = 0; count < arraySize; count++) {
			switch (state) {
			case 0: // Before local maximum
				if (m_lpFFT_Out[count] > cutoffValue)
					state = 1;
				else
					m_lpFFT_Out[count] = 0;
				break;

			case 1: // In local maximum
				if (m_lpFFT_Out[count] < cutoffValue)
					state = 2;
				break;

			case 2:
				m_lpFFT_Out[count] = 0;
				break;
			}
		}
	}

	public boolean ComputeAudio() {
		for (int nDex = 0; nDex < m_dwAudioSampleSize; nDex++)
			m_lpAudioSampleBuf[nDex] = 0;

		// Get input... fft array of audio levels (-1.0,1.0]
		if (targetDataLine.available() >= (m_dwAudioSampleSize / 2))
			if (targetDataLine.read(m_lpAudioSampleBuf, 0, m_dwAudioSampleSize) == m_dwAudioSampleSize) {
				int i;
				ByteBuffer bufferOfBytes = null;
				bufferOfBytes = ByteBuffer.wrap(m_lpAudioSampleBuf);
				bufferOfBytes.order(ByteOrder.LITTLE_ENDIAN);
				ShortBuffer bufferOfShorts = bufferOfBytes.asShortBuffer();
				for (i = 0; i < bufferOfShorts.capacity(); i++) {
					// 24 and 8 were removed to simplfy this release.
					if (m_dwDepth == 16)
						m_lpFFT_In[i] = (float) bufferOfShorts.get(i);
				}

				// convert data to [-1.0,1.0]
				for (i = 0; i < m_dwFFTDataSize; i++)
					m_lpFFT_In[i] /= (1 << (m_dwDepth - 1));

				// perform fft analysis
				ComputeFFT();
				return true;
			}
		return false;
	}

	// Methods used by FFT and translation.
	private void SetupReversedBitArray() {
		int index;
		int uIndex1;
		int uIndex2;

		// TODO: Put this back in for 1.5 compliancy
		// assert( m_lpReversedBits != null );

		if (m_lpReversedBits == null)
			return;

		for (uIndex1 = 0; uIndex1 < m_dwFFTDataSize; uIndex1++) {
			index = uIndex1;
			m_lpReversedBits[uIndex1] = 0;
			for (uIndex2 = 0; uIndex2 < DEFAULT_DSPT_FFTBITSIZE; uIndex2++) {
				m_lpReversedBits[uIndex1] = (m_lpReversedBits[uIndex1] << 1)
						| (index & 1);
				index = index >> 1;
			}
		}
	}

	private void ComputeFFT() {
		// leave variables static for quicker function execution
		float AngleNumerator = 6.283185f;

		// TODO: Put this back in for 1.5 compliance
		// assert( m_lpReversedBits != null );

		if (m_lpReversedBits == null)
			return;

		// setup for this fft
		for (i = 0; i < m_dwFFTDataSize; i++) {
			j = m_lpReversedBits[i];
			m_lpFFT_Out[j] = m_lpFFT_In[i];
			m_lpImageOut[j] = 0;
		}
		blockEnd = 1;
		blockSize = 2;

		// do fft
		while (blockSize <= m_dwFFTDataSize) {
			deltaAngle = AngleNumerator / (float) blockSize;
			alpha = (float) Math.sin(0.5f * deltaAngle);
			alpha = 2.0f * alpha * alpha;
			beta = (float) Math.sin(deltaAngle);
			i = 0;

			while (i < m_dwFFTDataSize) {
				ar = 1.0f;
				ai = 0.0f;
				j = i;

				for (n = 0; n < blockEnd; n++) {
					k = j + blockEnd;
					tr = (ar * m_lpFFT_Out[k]) - (ai * m_lpImageOut[k]);
					ti = (ai * m_lpFFT_Out[k]) + (ar * m_lpImageOut[k]);
					m_lpFFT_Out[k] = m_lpFFT_Out[j] - tr;
					m_lpImageOut[k] = m_lpImageOut[j] - ti;
					m_lpFFT_Out[j] = m_lpFFT_Out[j] + tr;
					m_lpImageOut[j] = m_lpImageOut[j] + ti;
					deltaAr = (alpha * ar) + (beta * ai);
					ai -= (alpha * ai) - (beta * ar);
					ar -= deltaAr;
					j++;
				}
				i += blockSize;
			}
			blockEnd = blockSize;
			blockSize <<= 1;
		}

		// compute spectrum
		for (i = 0; i < m_dwFFTDataSize; i++)
			m_lpFFT_Out[i] = (float) Math.sqrt(m_lpFFT_Out[i] * m_lpFFT_Out[i]
					+ m_lpImageOut[i] * m_lpImageOut[i]);

		findAndIsolateBand(m_dwFFTDataSize);
		ComputeDomFreq();
	}

	private void ComputeDomFreq() {
		// compute dominant frequency
		int windowSize = 4;
		float maxWeight = -1.0F;
		float centerFreq = -1.0F;
		float freq = 0.0f;

		for (int k = windowSize; k < m_dwFFTDataSize >> 1; k++) {
			int j = k - windowSize;

			float moment = 0.0F;
			float weight = 0.0F;

			for (; j <= k; j++) {
				weight += m_lpFFT_Out[j];
				moment += (float) j * m_lpFFT_Out[j];
			}

			if (weight > maxWeight) {
				maxWeight = weight;
				centerFreq = moment / weight;
			}
		}

		if (centerFreq != -1.0)
			freq = (float) m_dwRate * centerFreq / (float) m_dwFFTDataSize;

		m_strFreq = "";
		m_strNote = "-";
		m_floatPercentOff = 0.0f;

		if (freq > 13.5f) // corresponds to A(-1)... less than the lowest key
							// on a piano.
		{
			NumberFormat nf = NumberFormat.getInstance();
			nf.setMaximumFractionDigits(1);
			m_strFreq = nf.format(freq);
			m_strNote = Freq_to_note(freq, m_nInstrumentKey);
			nf.setMaximumFractionDigits(0);
			String m_strPercentOff = nf.format(m_floatPercentOff);
			bbtunerMain.labelPercentOff.setText(m_strPercentOff + "%");
			bbtunerMain.labelNote.setText(m_strNote);
			bbtunerMain.labelFreq.setText(m_strFreq + "Hz");
			bbtunerMain.updateDisplay(m_floatPercentOff);
		} else {
			bbtunerMain.labelPercentOff.setText("");
			bbtunerMain.labelNote.setText("-");
			bbtunerMain.labelFreq.setText("");
			bbtunerMain.offDisplay();
		}
	}

	private int Freq_to_note_index(double dblFreq) {
		double dnote = 12 * Math.log(dblFreq / m_dwRefFreq) / M_LN2;

		double center = Math.floor(dnote + 0.5);
		// Compute percentage off of frequency.
		m_floatPercentOff = 100.0f * (float) (dnote - center);

		for (int fudgeIndex = 1; fudgeIndex < fudgeFactors.length
				/ (Float.SIZE * 2); fudgeIndex++) {
			if (fudgeFactors[m_dwRefFreq - LOWEST_TUNE_FREQUENCY][fudgeIndex][0] > dblFreq) {
				m_floatPercentOff -= fudgeFactors[m_dwRefFreq
						- LOWEST_TUNE_FREQUENCY][fudgeIndex - 1][1];
				break;
			}
		}
		return 72 + (int) center;
	}

